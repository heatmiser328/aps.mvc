    // public class ApsDbInitializer : DropCreateDatabaseIfModelChanges<ApplicationDbContext>
    public class ApsDbInitializer : DropCreateDatabaseAlways<ApplicationDbContext>
    {
        protected override void Seed(ApplicationDbContext context)
        {
            InitializeIdentityForEF(context);
            base.Seed(context);
        }

        private void InitializeIdentityForEF(ApplicationDbContext context)
        {
            var UserManager = new UserManager<ApplicationUser>(new UserStore<ApplicationUser>(context));
            var RoleManager = new RoleManager<IdentityRole>(new RoleStore<IdentityRole>(context));            
            string name = "tom";
            string password = "Go123!";
            string admin = "Admin";

            // create admin role and user
            RoleManager.Create(new IdentityRole(admin));
            UserManager.Create(new ApplicationUser() { UserName = admin });

            // create default user
            var user = new ApplicationUser();
            user.UserName = name;
            var adminresult = UserManager.Create(user, password);

            //Add User Admin to Role Admin
            if (adminresult.Succeeded)
            {
                var result = UserManager.AddToRole(user.Id, admin);
            }
        }
    }

